/**
 *	from JAVA Loops: Exercise 2 ( https://www.w3schools.com/java/exercise.asp?filename=exercise_while_loop2 )
 *
 *	question:
 *	Use the do/while loop to print i as long as i is less than 6.
 *	
 *	int i = 1;
 *	__ {
 *		System.out.println(i);
 *		i++;
 *	}
 *	_____ (i < 6); 
 *
 */
int i = 1;
do {
	System.out.println(i);
	i++;
}
while (i < 6);